# 代替モジュール
"""
標準ライブラリモジュールはわかりにくいと思ったり、必要としている変換の機能がないと思うなら、サードパーティが作った
代替モジュールがたくさんある。その中のごく一部を紹介する。

    arrow: 多数の日時関数を単純なAPIで結び付けている。
    dateutil: このモジュールはほぼすべての日付の形式を解析し、相対的な日時を処理してくれる。
    iso8601: 標準ライブラリで手薄なISO601形式を十分にサポートする。
    fleming: 標準時間帯関連の関数を多数提供する。
    maya: 日時と時間感覚に対するわかりやすいインターフェースを持つ。
    dateinfer:  日時/時刻文字列から正しい書式文字列を推測する。
"""