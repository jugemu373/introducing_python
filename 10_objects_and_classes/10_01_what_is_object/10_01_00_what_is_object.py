# オブジェクトとは何か
"""
オブジェクトは、データ(変数、属性と呼ばれる)とコート(関数、メソッドと呼ばれる)の両方を含むカスタムデータ構造である。
何らかの具体的なものの実例(インスタンス)を表現する。オブジェクトを名詞、そのメソッドを動詞と考えるとよい。
オブジェクトは個別のものを表し、メソッドはほかのものとどのようなやり取りをするかを定義する。

例え、値7を持つ整数オブジェクトは加算や乗算などのメソッドをオブジェクトである。つまり、Pythonのどこかに整数のクラスがあり、
7や8はそこに属している。'cat'、'duck'などの文字列もPythonのオブジェクトであり、capitalize()や、replace()などのメソッドを持つ。

モジュールとは異なり、オブジェクト(インスタンスとも呼ばれる)は同時に複数持つことができる。それらのオブジェクトは、
それぞれ異なる属性を持っていてよい。
オブジェクトは、コードが放り込まれた超データ構造と言うことができるだろう。
"""